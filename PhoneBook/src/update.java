    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String N = n.getText();  // Text from the "num" field
String Phone = phone.getText();  // Text from the "phone" field
String Name = name.getText();  // Text from the "name" field

// Check if any of the fields are empty or null
if (N.isEmpty() || Phone.isEmpty() || Name.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill all fields before updating.");
    return;  // Exit if any field is empty
}

try {
    // Prepare the SQL statement
    pst = (PreparedStatement)con.prepareStatement("UPDATE final SET phone = ?, name = ? WHERE id = ?");

    // Set the values from text fields
    pst.setString(1, Phone);
    pst.setString(2, Name);
    pst.setString(3, N);

    // Execute the update
    pst.executeUpdate();

    // Inform the user
    JOptionPane.showMessageDialog(this, "Contact Successfully Updated!!");

    // Reset the form or return to previous screen
    Contact w = new Contact();
    this.hide(); 
    w.setVisible(true);  

    // Refresh the data
    Connect(); 

} catch (SQLException ex) {
    Logger.getLogger(update.class.getName()).log(Level.SEVERE, null, ex);
    JOptionPane.showMessageDialog(this, "Error updating the contact.");
}


    }//GEN-LAST:event_jButton1ActionPerformed

